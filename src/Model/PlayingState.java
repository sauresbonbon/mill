package Model;
/**
 * Enum representing the different phases in the game.
 * <ul>
 *     <li>{@code SETTING_PHASE}: Indicates that the player is in the setting phase.</li>
 *     <li>{@code MOVING_PHASE}: Indicates that the player is in the moving phase.</li>
 *     <li>{@code JUMPING_PHASE}: Indicates that the player is in the jumping phase.</li>
 * </ul>
 */
public enum PlayingState {

    SETTING_PHASE,
    MOVING_PHASE,
    JUMPING_PHASE
}
