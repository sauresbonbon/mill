package Model;
/**
 * Enum representing the possible states of a game field.
 * <ul>
 *     <li>{@code EMPTY}: Indicates that the field is empty.</li>
 *     <li>{@code BLACK}: Represents a field occupied by the black player.</li>
 *     <li>{@code WHITE}: Represents a field occupied by the white player.</li>
 * </ul>
 */
public enum FieldState {
    EMPTY,
    BLACK,
    WHITE
}
